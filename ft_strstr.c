#include "libft.h"

char	*ft_strstr(const char *big, const char *little)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if ((int)ft_strlen(little) == 0)
		return ((char*)big);
	while (big[i])
	{
		j = 0;
		while (little[j] && little[j] == big[i + j])
		{
			j++;
			if (little[j] == '\0')
				return ((char*)big + i);
		}
		i++;
	}
	return (NULL);
}
